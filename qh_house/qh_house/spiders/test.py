    name = 's'
    # allowed_domains = ['chinabdc.cn']
    # start_urls = ['http://chinabdc.cn/']

    def __init__(self, *args, **kwargs):
        super(BuildingSpider, self).__init__()
        self.result = dbz()

    def start_requests(self):
        for i in dbz()[5000:6000]:
            #栋楼链接
            new_url = 'https://www.chinabdc.cn/Tool/Config/QueryMethodName?methodName=GetUnitInfo&buildid='+i
            id = i
            yield scrapy.FormRequest(url=new_url, callback=self.parse_info,meta={'id':id,'house_url':new_url,})

    # 单元楼数据解析
    def parse_info(self, response):
        # print(response.text)
        id = response.meta['id']
        new_url = response.meta['house_url']
        js_text = json.loads(response.text)
        regulatorybank = jsonpath.jsonpath(js_text, '$..regulatorybank')
        regulatoryaccount = jsonpath.jsonpath(js_text, '$..regulatoryaccount')
        for regulatorybank, regulatoryaccount in zip(regulatorybank, regulatoryaccount):
            item = dict()
            item['regulatoryaccount'] = regulatoryaccount
            item['regulatorybank'] = regulatorybank
            item['id'] = id
            item['house_url'] = new_url
            yield item

