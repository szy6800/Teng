class DetailSpider(scrapy.Spider):
    name = 'detail'
    allowed_domains = ['baidu.com']

    def __init__(self, *args, **kwargs):
        super(DetailSpider, self).__init__()
        self.result = lists

#4353
    def start_requests(self):
        for i in self.result[6800:6866]:
            item = dict()
            item['ids'] = i
            # print(item['ids'])
            # 
            url = 'https://xkz.cbirc.gov.cn/bx/EKXEKX/showLicenceInfo.do?id={}'.format(item['ids'])
            yield scrapy.Request(url, callback=self.parse, dont_filter=True,
                                 meta={'item': copy.deepcopy(item)})

    def parse(self, response, *args):
        item = response.meta['item']
        item['now_title'] = response.xpath('//*[@class="trw-table-s1"]//tr[3]/td[2]/text()').get().strip()
        item['now_add'] = response.xpath('//*[@class="trw-table-s1"]//tr[4]/td[2]/text()').get().strip()
        try:
            item['old_title'] = response.xpath('//*[@id="td2"]//*[contains(text(),"")]/following::td[1]/text()').get().strip()
        except:
            item['old_title'] = ''
        try:
            item['old_add'] = response.xpath('//*[@id="td3"]//*[contains(text(),"")]/following::td[1]/text()').get().strip()
        except:
            item['old_add'] = ''
        yield item


